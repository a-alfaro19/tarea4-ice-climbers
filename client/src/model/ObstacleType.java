package model;

/**
 * Enumeración que representa los tipos de obstáculos que pueden aparecer en el juego iCE Climber.
 */
public enum ObstacleType {

    /** Representa un Yeti, enemigo que se mueve y puede causar daño. */
    YETI,

    /** Representa un ave (Bird), que vuela por la pantalla como obstáculo. */
    BIRD,

    /** Representa un bloque de hielo móvil o caído. */
    ICE_BLOCK
}
